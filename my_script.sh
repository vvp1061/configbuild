#!/bin/bash

# --- ШАГ 1: Проверка входного аргумента ---
# Проверяем, что аргумент ($1) не пустой
if [ -z "$1" ]; then
  echo "Ошибка: Не указана опция."
  echo "Пример использования: $0 3" # $0 - это имя самого скрипта
  exit 1 # Выходим с кодом ошибки
fi

# Сохраняем аргумент в переменную для удобства
user_choice=$1

# --- ШАГ 2: Основная логика скрипта ---
# Используем конструкцию 'case' для обработки разных вариантов выбора

echo "Скрипт запущен с опцией: $user_choice"
echo "-------------------------------------"

case $user_choice in
  1)
    echo "Выбрана Опция 1: Показать информацию о системе."
    uname -a
    ;;
  2)
    echo "Выбрана Опция 2: Показать использование дискового пространства."
    df -h
    ;;
  3)
    echo "Выбрана Опция 3: Показать информацию о памяти."
    free -h
    ;;
  4)
    echo "Выбрана Опция 4: Показать последние 5 записей из системного журнала."
    journalctl -n 5 --no-pager
    ;;
  *)
    # Этот блок сработает, если передано число, отличное от 1-4
    echo "Ошибка: Неверная опция. Пожалуйста, выберите число от 1 до 4."
    exit 1
    ;;
esac

echo "-------------------------------------"
echo "Выполнение скрипта успешно завершено."
